// 第一行的第三個單字必須和所在文件名稱完全相同，大小寫都要一樣
//public class後面代表定義一個類的名稱
//類:是java中所有source code的基本組織單位
public class HelloWorld {

	//第二行的內容是萬年不變的固定寫法，代表main方法
	// main代表程式碼執行的起點，如果沒有，則是沒辦法執行的
	public static void main (String[] args) {
		//第三行代表輸出print語句
		//System.out.println也為萬年不變的語句來輸出顯示
		System.out.println("Helloooooooo, World!");
	}
}
