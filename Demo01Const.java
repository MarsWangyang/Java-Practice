public class Demo01Const {
  public static void main(String[] args) {
    //字符串常量
    System.out.println("ABC");
    System.out.println(""); //字符串兩個雙引號中內容為空
    System.out.println("Z<C");

    //數字常量
    System.out.println(-100);
    System.out.println(200.123);
    System.out.println(-234.456);

    //字符常量
    System.out.println('A');
    System.out.println('6');
    // System.out.println(''); 兩個單引號中間必須有且僅有一個字符，不行沒有。
    // System.out.println('AB'); 兩個單引號中間必須有且僅有一個字符，不行沒有。

    // Boolin常數
    System.out.println(true);
    System.out.println(false);

    // 空常量，空常量不能夠直接用來輸出。
    // System.out.println(null);
    System.out.print("I love U");
    System.out.println("I love U");
    System.out.print("I love U very much");

  }
}
